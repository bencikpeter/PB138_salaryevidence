package Logic;

/**
 * Object represents one working day.
 * @author Peter Tirala
 */
public class Day {
    private int hours;
    private int date; // int just for now

    public int getHours() {
        return hours;
    }

    public void setHours(int hours) {
        this.hours = hours;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }
    
    
}
